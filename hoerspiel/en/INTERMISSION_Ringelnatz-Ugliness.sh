# DE voices: Anna Petra Markus Yannick

# Studio assistant 
SIE=anna
# Wife
FRAU=petra
BRIAN=Yannick
BURT=daniel
# Husband
JOHN=fred

source APPLE_BUG_Workarounds_etc.sh

# Sie ist die aktuelle Flamme von John. Er ist da was am Köcheln, Sparflamme.
say -v $SIE "Johnny boy! Johnny Pony! Wo bist Du Schatzi?"

say -v $BRIAN "Schnell John, versteck Dich!"

say -v $BURT "What?"

say -v $BRIAN "I meant John!"

say -v $JOHN "You annoy me. What's up?"

say -v $BRIAN "Deine Flamme, Alter!"

say -v $JOHN "Come again?"

say -v $BRIAN "Zu spät..."

say -v $SIE "Da bist Du ja, Johnny!"

say -v $JOHN "Why did nobody warn me? Nice friends I have here!"

say -v $FRAU "John, wouldn't you like to introduce me to your friends here @Twitter?"

say -v $BURT "Houston we have a problem?"

say -v $JOHN "Indeed. Burt, I mean Daniel. Now that you took my job as narrator. Go ahead. Make my day. Some Ringelnatz, please!"

say -v $BURT "My name is Burt and I am an english voice, sir."

say -v $JOHN "Drat."

say -v Princess "Now $SIE re-enters the scene, holding up a proposal!"

say -v $SIE "Ich könnte Ringelnatz für Dich lesen Johnny!"

afplay samples/HEULEN.WAV

say -v $JOHN "Yes that would be sweet for sure. Do!"

say -v $SIE "Ich bin ja so aufgeregt, John!"

afplay samples/HEULEN.WAV

say -v $SIE "Gut. Joachim Ringelnatz. Gedicht zum Thema: Hässlichkeit mit dem klangvollen Namen: Genau besehen."
sleep 3
say -v $SIE "Seid ihr bereit?"
sleep 2
afplay samples/HEULEN.WAV
say -v $SIE "Ok, ich fange an!"
sleep 1.5
say -v $SIE "Genau besehn"
sleep 1.5
say -v $SIE "Wenn man das zierlichste Näschen... Von seiner liebsten Braut... Durch ein Vergrößerungsgläschen... Näher beschaut,... Dann zeigen sich haarige Berge,... Daß einem graut."

afplay samples/HAUAB.WAV
